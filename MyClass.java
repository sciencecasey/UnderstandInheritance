
public class MyClass {
   private int x;
   private int y;
   
   MyClass(int v1, int v2) {
      this.x = v1;
      this.y = v2;
   }
   /**
    * @return the x
    */
   public int getX() {
      return this.x;
   }

   /**
    * @return the y
    */
   public int getY() {
      return this.y;
   }
}
